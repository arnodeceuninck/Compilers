def plus(self, args):
    return args[0] + args[1]


def min(self, args):
    return args[0] - args[1]


def mult(self, args):
    return args[0] * args[1]


def div(self, args):
    return args[0] / args[1]


def mod(self, args):
    return args[0] % args[1]


def minU(self, args):
    return -args[0]


def plusU(self, args):
    return +args[0]
